/*
    To extract Image from the browser
    */
    private void labelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_labelActionPerformed
        JFileChooser file = new JFileChooser(); //JFileChooser to extract the a image file
        file.setCurrentDirectory(new File(System.getProperty("user.home")));// to direct to the home
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.IMAGE","jpg","gif","png"); //to extact only the following extensions
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath(); 
            ImageIcon icon = new ImageIcon(path);
            label1.setIcon(icon);
            
            
        }
        else if(result == JFileChooser.CANCEL_OPTION){
            JOptionPane.showMessageDialog(null,"No File Select");
        }
    }//GEN-LAST:event_labelActionPerformed
