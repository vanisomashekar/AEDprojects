    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        flight.setFlightID(flightId.getText());
        flight.setDate(date.getDate());
        flight.setDepartLocation(departLoc.getText());
        flight.setArrivalLocation(arrivalLoc.getText());
        flight.setDepartTime(time.getText());
        flight.setPrice(Integer.parseInt(Price.getText()));
        flight.setAvailableSeats(Integer.parseInt(AvailableSeats.getText()));
        flightId.setEnabled(false);
        date.setEnabled(false);
        departLoc.setEnabled(false);
        arrivalLoc.setEnabled(false);
        Price.setEnabled(false);
        AvailableSeats.setEnabled(false);
        time.setEnabled(false);
        save.setEnabled(false);
        JOptionPane.showMessageDialog(null,"Successfully Updated Flight Details");
    }//GEN-LAST:event_saveActionPerformed

